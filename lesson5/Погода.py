weather=input("Яка сьогодні погода?: ")

if weather == "сонячно":
    print("Вдягти футболку,шорти,сонячні окуляри,босоніжки")

elif weather == "дощ":
    print("Вдягти куртку,шапку або кепку,чоботи,джинси,взяти парасолю")

elif weather == "хмарно":
    print("Вдягти легеньку куртку або худі,спортивні штани,кросівки")

elif weather == "сніг":
    print("Вдягти теплу куртку,шапку,шарф,зимові чоботи")

else:
    print("Схоже ви не збираєтеся кудись йти")