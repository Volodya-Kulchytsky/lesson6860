way=input("Ви знаходитеся в печері,перед вами 3 шлях.Який шлях виберете?(1,2,3): ")

if way == "1":
    print("Вітаємо ви знайшли скарб!Ви тепер багаті!")

elif way == "2":
    print("Ви не знайшли скарб, але вибралися з печери неушкодженим")

elif way == "3":
    print("Ви потрапили в лабіринт пастку!Тепер думайте, як звідси вийти")

else:
    print("Ви не вибрали жоден шлях і залишилися в печері")